mean.panova.aic = function(datfram){
## ------------------------------------—•°
## 'datfram' need to be of class  c("data.frame","panova","coefstars")  -- see below
## ------------------------------------—•°
   v1 = unlist(datfram["AIC",colnames.class(datfram,"numeric")])
   mean(v1[!is.na(v1)])
} ##----END----##

## --------------------------------------------------------—•°
mean.panova.mse = function(datfram){
## ------------------------------------—•°
##
## ------------------------------------—•°
   v1 = unlist(datfram["MSE",colnames.class(datfram,"numeric")])
   mean(v1[!is.na(v1)])
} ##----END----##

